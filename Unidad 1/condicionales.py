"""Condicionales"""

a = 10
b = 5

if a == b: #igual a
    print("a es igual b")
elif a > b: #mayor que
    print("a es mayor que b")
elif a < b: #menor que
    print("a es menor que b")
elif a >= b: #mayor o igual que
    print("a es mayor o igual que b")
elif a <= b: #menor o igual que
    print("a es menor o igual que b")
elif a != b: #no es igual que
    print("a no es igual que b")
else:
    print("No se cumple ninguna condicion")